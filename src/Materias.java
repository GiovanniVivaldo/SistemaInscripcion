public class Materias 
{
    int id,grupo;
    String nombre;

    public Materias() {
    }

    public Materias(int id, String nombre, int grupo) {
        this.id = id;
        this.nombre = nombre;
        this.grupo = grupo;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public int getGrupo() {
        return grupo;
    }

    public void setGrupo(int grupo) {
        this.grupo = grupo;
    }
    
}
